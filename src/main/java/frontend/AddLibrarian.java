    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

            String id = LibrarianIDTextField.getText();
            String name = LibrarianNameTextField.getText();
            String Email = LibrarianEmailTextField.getText();
            String Address = LibrarianAddressTextField.getText();
            String PhoneNumber = LibrarianPhoneNumberTextField.getText();
            if (name.equals("") || id.equals("") || Email.equals("") || Address.equals("") || PhoneNumber.equals("")) {
                JOptionPane.showMessageDialog(null, "Some fields are empty");
                return;
            }
            librariansUser[] records =admin.getListOfLibrarians();
            for (librariansUser m : records) {
                if (m.SearchKey().equals(id)) {
                    JOptionPane.showMessageDialog(null, "The librarian with id" + LibrarianIDTextField.getText() + "already exists!");
                    LibrarianIDTextField.setText("");
                    LibrarianNameTextField.setText("");
                    LibrarianEmailTextField.setText("");
                    LibrarianAddressTextField.setText("");
                    LibrarianPhoneNumberTextField.setText("");
                    return;
                    
                }
            }
            admin.addLibrarian(id,  name, Email, Address, PhoneNumber);

            
            JOptionPane.showMessageDialog(null, "The librarian with id" + LibrarianIDTextField.getText() + "has been successfullyÂ added");
            LibrarianIDTextField.setText("");
            LibrarianNameTextField.setText("");
            LibrarianEmailTextField.setText("");
            LibrarianAddressTextField.setText("");
            LibrarianPhoneNumberTextField.setText("");
              }//GEN-LAST:event_jButton1ActionPerformed

